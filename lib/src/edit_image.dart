                        toolTip: 'Flip Horizontal',
                        margin: const EdgeInsets.only(
                            left: 10.0, top: 0.0, right: 10.0, bottom: 0.0),
                        isActive: scaleX.isNegative,
                        onTap: () {
                          if (scaleX.isNegative) {
                            setState(() => scaleX = 1);
                          } else {
                            setState(() => scaleX = -1);
                          }
                        }),
                    CustomIconButton(
                        icon: CupertinoIcons.chevron_up_chevron_down,
